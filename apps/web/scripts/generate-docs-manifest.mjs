#!/usr/bin/env node
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const appRoot = path.resolve(__dirname, '..');
const configPath = path.join(appRoot, 'docs.config.json');
const outputPath = path.join(appRoot, 'src/lib/generated/docs-manifest.ts');

/**
 * @typedef {{ slug: string; name: string; category?: string; items?: DocItem[] }} DocItem
 */

function invariant(condition, message) {
	if (!condition) {
		throw new Error(message);
	}
}

/**
 * @param {string} value
 */
function escapeString(value) {
	return value.replaceAll('\\', '\\\\').replaceAll("'", "\\'");
}

/**
 * @param {unknown} value
 * @param {number} indentLevel
 * @returns {string}
 */
function toTs(value, indentLevel = 0) {
	const indent = '\t'.repeat(indentLevel);
	const childIndent = '\t'.repeat(indentLevel + 1);

	if (Array.isArray(value)) {
		if (value.length === 0) return '[]';
		const rows = value.map((item) => `${childIndent}${toTs(item, indentLevel + 1)}`);
		return `[\n${rows.join(',\n')}\n${indent}]`;
	}

	if (value && typeof value === 'object') {
		const entries = Object.entries(/** @type {Record<string, unknown>} */ (value)).filter(
			([, propValue]) => propValue !== undefined
		);
		if (entries.length === 0) return '{}';

		const rows = entries.map(
			([key, propValue]) => `${childIndent}${key}: ${toTs(propValue, indentLevel + 1)}`
		);
		return `{\n${rows.join(',\n')}\n${indent}}`;
	}

	if (typeof value === 'string') {
		return `'${escapeString(value)}'`;
	}

	if (typeof value === 'number' || typeof value === 'boolean') {
		return String(value);
	}

	if (value === null) return 'null';
	throw new Error(`Unsupported value in docs config: ${String(value)}`);
}

/**
 * @param {unknown} value
 * @param {string} pathLabel
 * @returns {DocItem}
 */
function parseDocItem(value, pathLabel) {
	invariant(typeof value === 'object' && value !== null, `${pathLabel} must be an object`);

	const item = /** @type {Record<string, unknown>} */ (value);
	invariant(typeof item.slug === 'string', `${pathLabel}.slug must be a string`);
	invariant(item.slug.trim().length > 0 || item.slug === '', `${pathLabel}.slug cannot be empty`);
	invariant(typeof item.name === 'string', `${pathLabel}.name must be a string`);
	invariant(item.name.trim().length > 0, `${pathLabel}.name cannot be empty`);

	if (!('items' in item) || item.items === undefined) {
		return { slug: item.slug, name: item.name };
	}

	invariant(Array.isArray(item.items), `${pathLabel}.items must be an array`);

	return {
		slug: item.slug,
		name: item.name,
		items: item.items.map((child, index) => parseDocItem(child, `${pathLabel}.items[${index}]`))
	};
}

/**
 * @param {DocItem[]} items
 * @param {string | undefined} category
 * @returns {DocItem[]}
 */
function flattenManifest(items, category) {
	return items.reduce((acc, item) => {
		if (item.items?.length) {
			acc.push(...flattenManifest(item.items, item.name));
			return acc;
		}

		acc.push({
			slug: item.slug,
			name: item.name,
			category
		});
		return acc;
	}, /** @type {DocItem[]} */ ([]));
}

/**
 * @param {DocItem[]} manifest
 */
function assertUniqueLeafSlugs(manifest) {
	const seen = new Set();
	for (const item of manifest) {
		invariant(!seen.has(item.slug), `Duplicate leaf slug in navigation config: "${item.slug}"`);
		seen.add(item.slug);
	}
}

async function main() {
	const raw = await readFile(configPath, 'utf8');
	const parsed = JSON.parse(raw);

	invariant(typeof parsed === 'object' && parsed !== null, 'docs.config.json must be an object');
	const config = /** @type {Record<string, unknown>} */ (parsed);

	invariant(Array.isArray(config.navigation), 'docs.config.json must define a "navigation" array');
	const docsNavigation = config.navigation.map((item, index) =>
		parseDocItem(item, `navigation[${index}]`)
	);
	const docsManifest = flattenManifest(docsNavigation);
	assertUniqueLeafSlugs(docsManifest);

	const generated = `/* This file is auto-generated by scripts/generate-docs-manifest.mjs. Do not edit manually. */
import type { DocItem } from '$lib/types/doc';

export const docsNavigation: DocItem[] = ${toTs(docsNavigation)};

export const docsManifest: DocItem[] = ${toTs(docsManifest)};
`;

	await mkdir(path.dirname(outputPath), { recursive: true });
	await writeFile(outputPath, generated, 'utf8');

	console.log(
		`Generated ${path.relative(appRoot, outputPath)} from ${path.relative(appRoot, configPath)}`
	);
}

main().catch((error) => {
	console.error(error instanceof Error ? error.message : String(error));
	process.exitCode = 1;
});
